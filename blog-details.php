									<h3>Forex Vs Stocks</h3>
									<p>
										The main reason why anyone would be interested in the stock
										or forex market is most likely to make money, or to hedge
										trade. But how exactly does one make money trading in one of
										the mentioned markets? Will this help determine which market
										is better?
									</p>
									<h3>How can I make money Forex trading?</h3>
									<p>
										The answer to this question is very simple for both stock
										and forex trading: buy low, sell high. However, the
										difficult and more complex part is actually finding the
										‘highs’ and ‘lows.’ There are two main ways to do this:
										technical and fundamental analysis. There is no clear-cut
										answer about which ideology is better, and thus the debate
										between technical and fundamental analysis is highly
										dependent on the individual and their personality.
									</p>
									<p>
										Both technical and fundamental analyses aim to predict
										future price movements based on the principles of supply and
										demand.
									</p>
									<h3>Technical Analysis</h3>
									<p>
										Technical analysis is the use of past data (such as price or
										volume) with the objective of predicting future price
										movements.
									</p>
									<p>
										In short, technical analysts base their trading style on the
										saying “history tends to repeat itself”.
									</p>
									<p>
										By examining past data, traders are sometimes able to
										identify highs and lows, so that they can buy and sell at
										the best times to make the most amount of profit.
									</p>
									<p>
										Technical analysis is applicable to both the stock and forex
										market (as well as other markets), however, it is primarily
										used by forex traders. Those who are interested in
										mathematics, data science, and pattern recognition may also
										find interest in technical analysis.
									</p>
									<h3>Fundamental Analysis</h3>
									<p>
										On the other hand, you also have fundamental analysis. This
										is generally performed on businesses and takes a holistic
										approach in determining the value of a security.
									</p>
									<p>
										For example, an investor (or trader) would examine the
										financial performance of a company, calculating values such
										as net profit, equity ratios et cetera.
									</p>
									<p>
										They would also take into account the company itself and its
										market - are people interested in this company? How many
										people will benefit from its actions?
									</p>
									<p>
										Macro and microeconomic factors are also taken into
										consideration (as well as many other factors), and used to
										determine a company’s potential or future value. Those who
										are interested in business and long-term investors may find
										that the stock market is more suitable for them.
									</p>
