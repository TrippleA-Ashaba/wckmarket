									<p>
										The forex or ‘foreign exchange’ market is a marketplace in
										which currencies can be bought, sold, and exchanged. The
										participants in this market range from banks, individual
										retail traders, and even travelers in need of local
										currency.
									</p>

									<p>
										The price at which the market participants buy or sell
										currencies is determined by its exchange rate, which is the
										value of one currency in terms of another - how much of one
										currency you need to buy one unit of another currency.
									</p>
									<p>
										For example, the Euro is worth 1.21 U.S. Dollars, meaning it
										takes $1.21 to buy one Euro. On a daily basis, trades are
										performed millions of times by many different individuals
										and corporations - on average, the forex market trades
										upwards of 5 trillion U.S. Dollars per day! This ‘price’
										however is not fixed and always changes.
									</p>

									<blockquote>
										<p>This ‘price’ however is not fixed and always changes.</p>
									</blockquote>

									<p>
										This is because exchange rates are driven based on supply
										and demand - the higher the demand, the higher the price,
										and vice versa. Exchange rates can change depending on
										things such as world events, news as well as other
										(sometimes even unknown) factors, though changes on a daily
										basis are generally very small. Most forex traders hold
										their positions for a few hours or days.
									</p>

									<h3>What is the stock market?</h3>
									<p>
										If you purchase a stock or ‘share’ of a company, you are
										essentially buying a piece of it. The more shares in a
										company you purchase, the greater your percentage of
										ownership. Most companies also pay out dividends to
										stockholders on an annual or quarterly basis.
									</p>
									<p>
										A dividend is a sum of money - the greater your ownership,
										the more money you would receive. Stock market traders
										generally hold their positions for several days - all the
										way up to months.
									</p>
									<p>
										The stock market is a collection of exchanges that trade
										various stocks in different companies. Shares can be bought
										and sold to others via this network of exchanges (similarly
										to buying and selling currencies). Unlike the foreign
										exchange market, which operates 24 hours a day every
										weekday, the stock market is open for 8 hours, 9:30 a.m. to
										4 p.m. Eastern time (on weekdays).
									</p>
									<p>
										The volume traded per day is $200 billion, considerably less
										than the $5 trillion traded in the forex market. If you
										would like a piece of the billions (and trillions) of
										Dollars traded per day, you may consider learning to trade
										stocks or forex.
									</p>
